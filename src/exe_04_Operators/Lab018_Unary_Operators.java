package exe_04_Operators;

public class Lab018_Unary_Operators
{
    public static void main(String[] args) {
        // Unary Operators -> - or + values with numbers get calculated.
        int a = +10;
        // int a = +10; + is optional
        int a1 = -110;
        // int a1 = -100l - is manditory
        int result = a+a1;
        System.out.println(a);
        System.out.println(a1);
        System.out.println(result);
    }
}
